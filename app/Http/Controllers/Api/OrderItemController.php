    #[Put('/update-order', middleware: ['permission:orders.edit'])]
    public function updateMultipleItemStatus(Request $request): JsonResponse
    {
        $data = $request->validate([
            'items' => 'required|array',
            'items.*.status' => 'nullable|integer|in:0,1,2,3',
            'items.*.quantity' => 'nullable|numeric|min:1',
            'items.*.notes' => 'nullable|string', // üÜï th√™m validate ghi ch√∫
        ]);

        $items = $data['items'];
        $updatedItems = [];
        $orderId = null;

        /** @var array<int|string, string> $errors */
        $errors = [];

        foreach ($items as $orderItemId => $itemData) {
            if (!is_array($itemData)) continue;

            $status = $itemData['status'] ?? null;
            $quantity = $itemData['quantity'] ?? null;
            $notes = $itemData['notes'] ?? null; // üÜï l·∫•y ghi ch√∫

            $orderItem = OrderItem::find($orderItemId);

            if (!$orderItem) {
                $errors[$orderItemId] = "OrderItem $orderItemId kh√¥ng t·ªìn t·∫°i.";
                continue;
            }

            // Ki·ªÉm tra tr·∫°ng th√°i h·ª£p l·ªá tr∆∞·ªõc khi c·∫≠p nh·∫≠t
            if ($status !== null) {
                if ($orderItem->status === 2 && $status === 3) {
                    $errors[$orderItemId] = "M√≥n {$orderItem->dish_id} ƒë√£ ph·ª•c v·ª•, kh√¥ng th·ªÉ h·ªßy.";
                    continue;
                }

                if ($orderItem->status === 3 && $status !== 3) {
                    $errors[$orderItemId] = "M√≥n {$orderItem->dish_id} ƒë√£ h·ªßy, kh√¥ng th·ªÉ thay ƒë·ªïi tr·∫°ng th√°i.";
                    continue;
                }

                $orderItem->status = $status;

                // Ghi th·ªùi ƒëi·ªÉm Served
                if ($status == 2 && !$orderItem->served_at) {
                    $orderItem->served_at = now();
                }
            }

            // üÜï C·∫≠p nh·∫≠t ghi ch√∫ n·∫øu c√≥
            if ($notes !== null) {
                $orderItem->notes = $notes;
            }

            if (is_numeric($quantity) && $quantity > 0) {
                $orderItem->quantity = $quantity;
                $orderItem->total_price = $orderItem->price * $quantity;
            }

            $orderItem->save();
            $updatedItems[] = $orderItem;

            if (!$orderId) {
                $orderId = $orderItem->order_id;
            }
        }

        // C·∫≠p nh·∫≠t Order n·∫øu c√≥
        if ($orderId) {
            $order = Order::with('items')->find($orderId);

            if ($order) {
                $statuses = $order->items->pluck('status')->unique()->sort()->values()->all();

                $orderStatus = match (true) {
                    $statuses === [3] => 4, // Cancelled
                    collect($statuses)->every(fn($s) => in_array($s, [2, 3])) => 2, // Served
                    in_array(1, $statuses) => 1, // In-progress
                    $statuses === [0] => 0, // Open
                    default => 1, // Mixed
                };

                $order->status = $orderStatus;
                // Ch·ªâ c·ªông t·ªïng ti·ªÅn c√°c m√≥n ch∆∞a b·ªã h·ªßy
                $order->total_amount = $order->items
                    ->where('status', '!=', 3)
                    ->sum('total_price');
                $order->save();
            }
        }

        // N·∫øu c√≥ l·ªói t·ª´ng m√≥n, tr·∫£ v·ªÅ m·∫£ng l·ªói
        if (!empty($errors)) {
            return $this->errorResponse(
                'M·ªôt s·ªë m√≥n kh√¥ng th·ªÉ c·∫≠p nh·∫≠t tr·∫°ng th√°i', // message string
                $errors, // m·∫£ng l·ªói chi ti·∫øt
                422
            );
        }

        return $this->successResponse(
            $updatedItems,
            'Order items, quantities, and order total updated successfully.'
        );
    }

